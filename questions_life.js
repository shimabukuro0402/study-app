// 学習指導要領に基づく問題プールを初期化
function initLifeQuestions() {
  const grades = [1,2];
  const terms = [1,2,3];
  const diffs = ['easy','normal','hard'];
  for (const g of grades) {
    Q_LIFE[g] = {};
    for (const t of terms) {
      Q_LIFE[g][t] = {};
      for (const d of diffs) {
        Q_LIFE[g][t][d] = generateLifePool(g, t, d);
      }
    }
  }
  return Q_LIFE;
}

