// 学習指導要領に基づく問題プールを初期化
function initSocialQuestions() {
  const grades = [3,4,5,6];
  const terms = [1,2,3];
  const diffs = ['easy','normal','hard'];
  for (const g of grades) {
    Q_SOCIAL[g] = {};
    for (const t of terms) {
      Q_SOCIAL[g][t] = {};
      for (const d of diffs) {
        Q_SOCIAL[g][t][d] = generateSocialPool(g, t, d);
      }
    }
  }
  return Q_SOCIAL;
}

