const Q_JAPANESE = {};

function initJapaneseQuestions() {
  const grades = [1,2,3,4,5,6];
  const terms = [1,2,3];
  const diffs = ['easy','normal','hard'];
  for (const g of grades) {
    Q_JAPANESE[g] = {};
    for (const t of terms) {
      Q_JAPANESE[g][t] = {};
      for (const d of diffs) {
        Q_JAPANESE[g][t][d] = generateJapanesePool(g, t, d);
      }
    }
  }
  return Q_JAPANESE;
}

